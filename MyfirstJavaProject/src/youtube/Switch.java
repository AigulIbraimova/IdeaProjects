package youtube;

public class Switch {
	public static void main(String[] args) {
		int age;
		age =5;
		switch (age) {
		case 1:
			System.out.println("You can walk");
			break;
		case 2:
			System.out.println("You can talk");
		    break;
		case 3:
			System.out.println("You can run");
			break;
		default:
			System.out.println("I don't know");
			break;
		
			
		}
	}

}
